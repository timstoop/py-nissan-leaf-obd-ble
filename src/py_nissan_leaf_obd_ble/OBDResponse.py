import logging
import time

logger = logging.getLogger(__name__)


class OBDResponse:
    """Standard response object for any OBDCommand."""

    def __init__(self, command=None, messages=None) -> None:
        """Initialise."""
        self.command = command
        self.messages = messages if messages else []
        self.value = None
        self.time = time.time()

